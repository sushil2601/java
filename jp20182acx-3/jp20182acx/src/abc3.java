public class abc3 {
  public static void main(String s[])
    {
        double x=Math.pow(3, 4);
        System.out.println(x);
        
        x=Math.sqrt(16);
        System.out.println(x);
        
        x=Math.toRadians(60);
        System.out.println(x);
        
        x=Math.toDegrees(x);
        System.out.println(x);
        
        x=Math.ceil(10.2);
        System.out.println(x);
       
        x=Math.floor(10.6);
        System.out.println(x);
       
        x=Math.round(10.6);
        System.out.println(x);
       
    }    
}
